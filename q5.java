public class q5 {
    static int add(int a, int b)
    {
        return a+b;
    }  
    static int sub(int a , int b)
    {
        return a-b;
    } 
    public static void main(String[] args)
    {
        int a = 10;
        int b=20;
        
        System.out.println("Result of adding :" + add(a,b));
        System.out.println("Result of subtraction :" + sub(a,b));
    }
}
